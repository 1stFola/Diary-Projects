package MyersBriggs;

public class MyerBriggsQuestions {

    String questionPrompt;
    String answersA;
    String answersB;
    String answersC;



    public MyerBriggsQuestions() {

    }

    public MyerBriggsQuestions(String questionPrompt, String answersA, String answersB, String answersC) {
        this.questionPrompt = questionPrompt;
        this.answersA = answersA;
        this.answersB = answersB;
        this.answersC = answersC;
    }




}
